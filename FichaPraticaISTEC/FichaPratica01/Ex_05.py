# Ler valores
valor1 = float(input("Insira valor1: "))
valor2 = float(input("Insira valor2: "))
valor3 = float(input("Insira valor3: "))

# Calcular a media aritmetica
mediaAritmetica = (valor1+valor2+valor3)/3

# Apresentar resultados
print("Média Aritmética:",mediaAritmetica)

# Calcular a media ponderada
mediaPonderada = (valor1*0.2)+(valor2*0.3)+(valor3*0.5)

# Apresentar resultados
print("Média Ponderada:",mediaPonderada)
